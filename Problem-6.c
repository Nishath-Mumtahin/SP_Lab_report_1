// Write a C program to convert specified days into years, weeks and days. Note: Ignore leap year.

#include <stdio.h>
int main()
{
    int totalDays, years, weeks, days;
    printf("Number of days: ");
    scanf("%d", &totalDays);
    printf("Years: ");
    scanf("%d", &years);
    printf("Weeks: ");
    scanf("%d", &weeks);
    printf("Days: ");
    scanf("%d", &days);


    return 0;
}